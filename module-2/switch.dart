// 17. Write Program use switch statement. Display Monday to Sunday

void main()
{

  int day=2;
  switch(day)
      {
    case 1:
      print("monday");
      break;
    case 2:
      print("Tuesday");
      break;
    case 3:
      print("Wednesday");
      break;
    case 4:
      print("Thursday");
      break;
    case 5:
      print("Friday");
      break;
    case 6:
      print("Saturday");
      break;
    case 7:
      print("Sunday");
      break;
    default:
      print("Invalid day");


  }



}