// 20.Write a program to print the 1 to 10 using For loop.
void main()
{
  for(int i=1;i<=10;i++)
    {
      print(i);
    }

  // Write a Program to print the 51 to 60 using while loop
  int i=51;
  while(i<=60)
  {
    print(i);
    i++;
  }

  // Write a program to print the 100 to 81 using do while loop
int n=100;
  do
    {
      print(n);
      n--;

    }while(n>=81);

    }